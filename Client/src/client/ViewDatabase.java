    private void txtFilterInputKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFilterInputKeyReleased
        // TODO add your handling code here:
        String temp = txtFilterInput.getText() + "%";
        String sql=null;
        if(FilterType.getSelectedIndex()== 1) 
            sql = "select * from Cars where Марка like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 2) 
            sql = "select * from Cars where Модель like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 3) 
            sql = "select * from Cars where ГодВыпуска like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 4) 
            sql = "select * from Cars where Класс like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 5) 
            sql = "select * from Cars where ОбъёмДвигателя like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 6) 
            sql = "select * from Cars where Цвет like'"+temp+"'";

        if(FilterType.getSelectedIndex()== 7) 
            sql = "select * from Cars where Цена like'"+temp+"'";
        
        if(FilterType.getSelectedIndex()== 8)
            sql = "select * from Cars where Статус like'"+temp+"'";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));

        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtFilterInputKeyReleased

