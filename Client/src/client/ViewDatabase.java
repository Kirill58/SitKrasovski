    private void statusTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusTypeActionPerformed
        String tmp = (String) statusType.getSelectedItem();
        String sql= "Update Cars set Статус='"+tmp+"' where ID = "+Id+"";
        try{
            pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Статус изменён");
            currentStatus.setText(tmp);
            UpdateJTable();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }
    }//GEN-LAST:event_statusTypeActionPerformed

